var flag = false
var error = document.getElementById("error");
function searchData() {
    var sData = document.getElementById('search').value;
    if (sData !== undefined && sData !== "" && sData !== " ") {
        error.innerHTML = ""
        for (var key in cellPhone) {
            for (var key2 in cellPhone[key]) {
                for (var key3 in cellPhone[key][key2]) {
                    if (key3.toLowerCase() === sData.toLowerCase()) {
                        data.innerHTML = "";
                        data.innerHTML += "<tr>" +
                            "<td>" +
                            key3
                            + "</td>"
                            + "<td>" +
                            cellPhone[key][key2][key3].Display.Size
                            + "</td>"
                            + "<td>" +
                            cellPhone[key][key2][key3].Memory.Built_in
                            + "</td>"
                            + "<td>" +
                            cellPhone[key][key2][key3].Camera.Main
                            + "</td>"
                            + "<td>" +
                            cellPhone[key][key2][key3].Price.Price_in_Rs
                            + "</td>"
                            + "</tr>";
                        flag = true;
                    }
                }
            }
        }
        if(flag === false){
            error.innerHTML = "Sorry not found..!"
        }
    } else {
        error.innerHTML = "Please enter complete model name..!"
    }
    
}